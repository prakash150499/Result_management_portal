        // TODO add your handling code here:
        String roll=roll_number.getText();
        String s1=sub1.getText();
        String s2=sub2.getText();
        String s3=sub3.getText();
        String s4=sub4.getText();
        String s5=sub5.getText();
        try
       {
         
           String url="jdbc:mysql://localhost:3306/srms2";
           String username="root";
           String password="Prakash@4064";
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=DriverManager.getConnection(url,username,password);
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select *from student where RollNumber='"+roll+"'");
           if(rs.next())
           {
               String query="insert into result(RollNumber,Subject1,Subject2,Subject3,Subject4,Subject5) values('"+ roll+"','"+ s1+"','"+ s2+"','"+ s3+"','"+ s4+"','"+ s5+"') ";
           st.executeUpdate(query);
           JOptionPane.showMessageDialog(null,"Marks saved successfully");
           setVisible(false);
           new addResult().setVisible(true);
               
           }
           else
           {
               JOptionPane.showMessageDialog(null,"Roll Number Not Registered");
               setVisible(false);
                new addResult().setVisible(true);
           }
           
          
          
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e.toString());
       }
        setVisible(false);
        new addResult().setVisible(true);
