    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno=rollNumber.getText();
           try
        {
           String url="jdbc:mysql://localhost:3306/srms2";
           String username="root";
           String password="Prakash@4064";
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=DriverManager.getConnection(url,username,password);
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select *from student inner join result where student.RollNumber='"+ rollno+"'and result.RollNumber='"+ rollno+"'");
           if(rs.next())
           {
               name.setText(rs.getString(2));
               course.setText(rs.getString(5));
               department.setText(rs.getString(6));
               fatherName.setText(rs.getString(3));
               gender.setText(rs.getString(4));
               subject1.setText(rs.getString(8));
               subject2.setText(rs.getString(9));
               subject3.setText(rs.getString(10));
               subject4.setText(rs.getString(11));
               subject5.setText(rs.getString(12));
               int s1=Integer.parseInt(subject1.getText());
               int s2=Integer.parseInt(subject2.getText());
               int s3=Integer.parseInt(subject3.getText());
               int s4=Integer.parseInt(subject4.getText());
               int s5=Integer.parseInt(subject5.getText());
               String totalMarks= String.valueOf(s1+s2+s3+s4+s5);
               total.setText(totalMarks);
           }
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e.toString());
       }
    }//GEN-LAST:event_formComponentShown

